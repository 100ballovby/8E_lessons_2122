# найти сумму чисел трехзначного числа
"""
462

462 / 100 = 4.62 (462 // 100 = 4)
462 / 10 = 46.2 (462 % 10 = 2) ❌
temp = 462 % 100 = 62
temp // 10 = 6
temp % 20 = 2
"""
n = int(input('Введите трехзначное число: '))
hundreds = n // 100  # сотни
temp = n % 100  # промежуточные вычисления
tens = temp // 10  # десятки
units = temp % 10  # единицы
print(f'{hundreds} * {tens} * {units} = {hundreds * tens * units}')

