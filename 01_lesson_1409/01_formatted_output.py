n1 = int(input('Введи число: '))
n2 = int(input('Введи число: '))

print(f'{n1} + {n2} = {n1 + n2}')
print(f'{n1} ** {n2} = {n1 ** n2}')
print(f'{n1} % {n2} = {n1 % n2}')
# операторы сравнения
print(f'{n1} > {n2}: {n1 > n2}')
print(f'{n1} < {n2}: {n1 < n2}')
print(f'{n1} >= {n2}: {n1 >= n2}')
print(f'{n1} <= {n2}: {n1 <= n2}')
print(f'{n1} != {n2}: {n1 != n2}')
print(f'{n1} == {n2}: {n1 == n2}')
